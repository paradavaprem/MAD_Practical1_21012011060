fun main()
{
    //integer to double
    val a : Int = 10
    println("$a is integer")
    val b:Double = a.toDouble()
    println("$b is now Double")

    //string to integer
    val c:String = "25"
    println("$c is string")
    val d:Int = c.toInt()
    println("$d is now Integer")

    //string to Double
    val e:String = "65.55"
    println("$c is string")
    val f:Double = e.toDouble()
    println("$f is now Double")
}
        